function z = blockchars(string, x, y)

L = length(string);
rows = size(x);
cols = rows(2);
rows = rows(1);

z = zeros([rows cols]);

drows = rows/10;
dcols = cols/(9*L);

for c = 1:L
    map = getmap(string(c));
    for i = 1:8
        for j = 1:7
            for y = ceil(i*drows)+1:ceil((i+1)*drows)
                for x = ceil((9*(c-1)+j)*dcols)+1:ceil((9*(c-1)+j+1)*dcols)
                    if map(i,j) == 1
                        z(rows-y+1,x) = 1;
                    end
                end
            end
        end
    end
end



