function test_convergence_lax_friedrichs(a, T, hs, lambda)

    uK = @(k,h,K,t,x) ...
         exp(-2*pi^2*K^2*(h^2/k-a^2*k)*t) ...
      .* cos(2*pi*K*(x - a*t));
    u = @(k,h,t,x) uK(k,h,1,t,x) - 2*uK(k,h,2,t,x) + 3*uK(k,h,3,t,x);
    test_convergence( ...
        a, T, u, ...
        @lax_friedrichs, "Lax-Friedrichs", ...
        hs, @(h) lambda*h);

endfunction
